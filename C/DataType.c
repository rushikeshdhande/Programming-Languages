#include <stdio.h>
int main()
{
    char cValue = 'S';
    int iValue = 10;
    float fValue = 3.14f;
    double dValue = 6.28634478;

    printf("Character: %c\n", cValue);
    printf("Integer: %d\n", iValue);
    printf("Float: %f\n", fValue);
    printf("Double: %f\n", dValue);

    return 0;
}